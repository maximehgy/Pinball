package tsp.flipper;

public class Point  
{ 
	double x; 
	double y; 

	//CONSTRUCTEUR
	public Point(double x2, double y2) 
	{ 
		this.x = x2; 
		this.y = y2; 
	} 
	
	// SETTER
	public void setX(double x2) {
		this.x = x2;
	}
	
	public void setY(double y2) {
		this.y = y2;
	}

}; 
